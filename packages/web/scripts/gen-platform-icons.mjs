// Regenerates app/components/platform-icons.ts from the real brand logos in
// ./platform-icons. Run manually on macOS (uses `sips` to rasterize/resize) when
// a logo changes or a provider is added:
//
//   node packages/web/scripts/gen-platform-icons.mjs
//
// The two color logos (Cloudflare orange, Supabase green) are embedded as small
// PNG data URIs; the two monochrome marks (Vercel, Railway) are embedded as SVG
// data URIs recolored white, since the graph's service container is always dark.
// Embedding as data URIs keeps a badge self-contained — no async asset fetch on
// the canvas, and no reliance on public/ shipping in the daemon-spawned build.
import { readFileSync, writeFileSync, mkdtempSync } from 'node:fs'
import { execSync } from 'node:child_process'
import { tmpdir } from 'node:os'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const HERE = dirname(fileURLToPath(import.meta.url))
const SRC = join(HERE, 'platform-icons')
const OUT = join(HERE, '..', 'app', 'components', 'platform-icons.ts')
const TMP = mkdtempSync(join(tmpdir(), 'neat-icons-'))

// color raster logo -> 48px PNG data URI (sips reads webp + png, keeps alpha).
function pngDataUri(srcFile, tmpName) {
  const tmp = join(TMP, tmpName)
  execSync(`sips -s format png -Z 48 "${join(SRC, srcFile)}" --out "${tmp}" >/dev/null 2>&1`)
  return `data:image/png;base64,${readFileSync(tmp).toString('base64')}`
}

const svgDataUri = (svg) => `data:image/svg+xml;utf8,${encodeURIComponent(svg)}`

const cloudflare = pngDataUri('cloudflare.png', 'cf.png')
const supabase = pngDataUri('supabase.webp', 'sb.png')

// Vercel: the triangle mark only (the source SVG is the full wordmark), white.
const vercelSvg =
  `<svg xmlns='http://www.w3.org/2000/svg' width='16' height='16' viewBox='0 0 467.444 406.664'>` +
  `<path fill='#fff' d='M467.444 406.664L233.722 0.190918L0 406.664H467.444Z'/></svg>`

// Railway: both silhouette paths from the source SVG, recolored white.
const railwaySrc = readFileSync(join(SRC, 'railway.svg'), 'utf8')
const railwayPaths = [...railwaySrc.matchAll(/<path d="([^"]+)"/g)].map((m) => m[1])
const railwaySvg =
  `<svg xmlns='http://www.w3.org/2000/svg' width='16' height='16' viewBox='0 0 1024 1024'>` +
  railwayPaths.map((d) => `<path fill='#fff' d='${d}'/>`).join('') +
  `</svg>`

const module = `// Real platform brand marks for the graph's service badges (#752 + ADR-133).
//
// GENERATED by scripts/gen-platform-icons.mjs from the logos in
// scripts/platform-icons — edit those + rerun, don't hand-edit the data URIs.
//
// The service container is always dark (a black compound / near-black collapsed
// node), so the two full-color logos — Cloudflare (orange) and Supabase (green)
// — read as-is, while the two monochrome marks — Vercel and Railway — render
// white to contrast, which is each brand's own dark-mode treatment. Embedded as
// data URIs so a badge ships self-contained: no async asset fetch on the canvas,
// no public/ dependency in the daemon-spawned web build. A new provider is a new
// source logo + a new entry here, keyed by ServiceNode.platform.
export const PLATFORM_ICON_DATA: Record<string, string> = {
  cloudflare: ${JSON.stringify(cloudflare)},
  supabase: ${JSON.stringify(supabase)},
  vercel: ${JSON.stringify(svgDataUri(vercelSvg))},
  railway: ${JSON.stringify(svgDataUri(railwaySvg))},
}
`

writeFileSync(OUT, module)
console.log('wrote', OUT)
